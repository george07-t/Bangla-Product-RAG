PRICE_RANGE = {
    "ফুড": (15, 500),
    "সবজি": (20, 200),
    "পার্সোনাল কেয়ার": (30, 1500),
    "ইলেকট্রনিক্স": (500, 150000),
    "পোশাক": (200, 5000),
    "স্টেশনারি": (5, 500),
    "ওষুধ": (10, 800),
}

def generate_products(n=5000):
    products = []
    seen_names = set()

    while len(products) < n:
        base_name, category = random.choice(PRODUCTS)
        brand = random.choice(BRANDS)
        unit = random.choice(UNITS.get(category, ["পিস"]))
        price_min, price_max = PRICE_RANGE.get(category, (50, 1000))
        price = round(random.uniform(price_min, price_max) / 5) * 5  # round to nearest 5

        full_name = f"{brand} {base_name}".strip() if brand else base_name

        # Add variants to reach 5000 unique products
        size_variants = ["", " (ছোট)", " (মাঝারি)", " (বড়)", " (৫০০ গ্রাম)", " (১ কেজি)", " (২৫০ মিলি)", " (৫০০ মিলি)", " (১ লিটার)"]
        full_name += random.choice(size_variants)

        if full_name in seen_names:
            continue

        seen_names.add(full_name)
        products.append({
            "id": len(products) + 1,
            "name": full_name,
            "category": category,
            "price": int(price),
            "unit": unit,
            "description": f"{full_name} - {category} পণ্য, প্রতি {unit} মূল্য {int(price)} টাকা"
        })

    return products


